public class Pessoa {
    String nome;
    String idade;
    String tamanho;

    public Pessoa(String nome, String idade, String tamanho) {
        this.nome = nome;
        this.idade = idade;
        this.tamanho = tamanho;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getIdade() {
        return idade;
    }

    public void setIdade(String idade) {
        this.idade = idade;
    }

    public String getTamanho() {
        return tamanho;
    }

    public void setTamanho(String tamanho) {
        this.tamanho = tamanho;
    }
}